<p>
    Wingfoil News sammelt relevante Neuigkeiten, Blog-Posts, Foren und Web-Seiten rund um das Thema Wingfoilen.
    Interessante Links könnt Ihr über den <a href="/publishlink">Publish-Link</a> einstellen. Ich prüfe und schalte den Link
    anschließend frei.
</p>

<p>
    Links können getaggt werden. Bitte verwendet die Tags
</p>
<ul>
    <li><strong>blog</strong> für Links auf Blog-Beiträge,</li>
    <li><strong>group</strong> für Links auf Foren-Beiträge und</li>
    <li><strong>site</strong> für Links auf Web-Seiten zum Thema Wingfoilen.</li>
</ul>

Die mit diesen "vordefinierten" Tags getaggten Links erscheinen unter den entsprechenden
Menü-Einträgen. Diese und alle anderen Links erscheinen im <a href="/">News-Bereich</a>,
sortiert nach Aktualität.

